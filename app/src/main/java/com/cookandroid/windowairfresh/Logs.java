package com.cookandroid.windowairfresh;

public class Logs {
    int icon;
    String alertxt;

    public Logs(int icon, String alertxt) {
        this.icon = icon;
        this.alertxt = alertxt;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getAlertxt() {
        return alertxt;
    }

    public void setAlertxt(String alertxt) {
        this.alertxt = alertxt;
    }
}
